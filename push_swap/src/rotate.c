#include "../inc/push_swap.h"

static void	rotate(t_stack **stack)
{
	t_stack *temp;
	t_stack *tail;

	temp = *stack;
	*stack = (*stack)->next;
	tail = get_bottom(*stack);
	temp->next = NULL;
	tail->next = temp;
}

void	do_ra(t_stack **stack_a)
{
	rotate(stack_a);
	ft_putstr("ra\n");
}

void	do_rb(t_stack **stack_b)
{
	rotate(stack_b);
	ft_putstr("rb\n");
}

void	do_rr(t_stack **stack_a, t_stack **stack_b)
{
	rotate(stack_a);
	rotate(stack_b);
	ft_putstr("rr\n");
}